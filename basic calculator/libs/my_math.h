

double sum(double num1, double num2)
{
    return num1 + num2;
}

double subt(double num1, double num2)
{
    return num1 - num2;
}

double divi(double num1, double num2)
{
    return num1 / num2;
}

int rest(int num1, int num2)
{
    return num1 % num2;
}



double mult(double num1, double num2)
{
    return num1 * num2;
}